#Exercice 2 Calculs simples
    #Opérations mathématiques
    #1. Demande deux nombres
    #2. Affiche les quatre opération de base

nombre_1 = int(input("Entre le premier nombre: "))
nombre_2 = int(input("Entre le deuxieme nombre: "))

somme = nombre_1 + nombre_2
difference = nombre_1 - nombre_2
produit = nombre_1 * nombre_2
division = nombre_1 / nombre_2

print(f"La somme de ces deux nombres est: {somme}")
print(f"La difference de ces deux nombres est: {difference}")
print(f"La produit de ces deux nombres est: {produit}")
print(f"La division de ces deux nombres est: {division}")